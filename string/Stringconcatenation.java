package string;

public class Stringconcatenation{
    

    public static void main(String[] args) {

        String brand = "Shivam";
        System.out.println(brand);

        StringBuilder brand1 = new StringBuilder("Shivam");
        System.out.println(brand1);
        brand1.append(" Gupta");
        System.out.println(brand1);
        

        
    }
}